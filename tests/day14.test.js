const day14 = require('../src/day14');

test('day 14-1 gets amount of ore', () => {
  const reactions = `
    10 ORE => 10 A
    1 ORE => 1 B
    7 A, 1 B => 1 C
    7 A, 1 C => 1 D
    7 A, 1 D => 1 E
    7 A, 1 E => 1 FUEL
  `;
  const formulae = day14.parse(reactions);
  expect(day14.ore(formulae)).toBe(31);
});

test('day 14-2 gets amount of fuel', () => {
  const reactions = `
    157 ORE => 5 NZVS
    165 ORE => 6 DCFZ
    44 XJWVT, 5 KHKGT, 1 QDVJ, 29 NZVS, 9 GPVTF, 48 HKGWZ => 1 FUEL
    12 HKGWZ, 1 GPVTF, 8 PSHF => 9 QDVJ
    179 ORE => 7 PSHF
    177 ORE => 5 HKGWZ
    7 DCFZ, 7 PSHF => 2 XJWVT
    165 ORE => 2 GPVTF
    3 DCFZ, 7 NZVS, 5 HKGWZ, 10 PSHF => 8 KHKGT
  `;
  const formulae = day14.parse(reactions);
  expect(day14.fuel(formulae, 1000000000000)).toBe(82892753);
});
